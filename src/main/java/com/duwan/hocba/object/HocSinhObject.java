package com.duwan.hocba.object;

public class HocSinhObject
{
	private int hocsinh_id;
	private String lop_id;
	private int phuhuynh_id;
	private String user_tendangnhap;

	public HocSinhObject()
	{}

	public int getHocsinh_id() {
		return hocsinh_id;
	}

	public void setHocsinh_id(int hocsinh_id) {
		this.hocsinh_id = hocsinh_id;
	}

	public String getLop_id() {
		return lop_id;
	}

	public void setLop_id(String lop_id) {
		this.lop_id = lop_id;
	}

	public int getPhuhuynh_id() {
		return phuhuynh_id;
	}

	public void setPhuhuynh_id(int phuhuynh_id) {
		this.phuhuynh_id = phuhuynh_id;
	}

	public String getUser_tendangnhap() {
		return user_tendangnhap;
	}

	public void setUser_tendangnhap(String user_tendangnhap) {
		this.user_tendangnhap = user_tendangnhap;
	}
	
}